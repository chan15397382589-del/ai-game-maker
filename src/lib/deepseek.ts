export async function createChatCompletion(messages: any[], currentCode?: string, group?: string) {
  let socraticMode = group === "high_srl";
  // 构建系统提示词
  let systemPrompt = TEACHER_SYSTEM_PROMPT;

  // 根据分组注入教学模式
  if (socraticMode) {
    systemPrompt += SOCRATIC_ADDON;
  } else {
    systemPrompt += DIRECTIVE_ADDON;
  }

  // 构思阶段（无代码）
  if (!currentCode) {
    systemPrompt += `\n\n⚠️ 构思阶段。学生表达了想做什么。直接做基础版！\n- 禁止反问"主角？障碍物？得分？"——一次只问一个问题\n- 问问题后绝对不要马上给A/B/C选项！让学生自己先想说\n- 学生说得够清楚了就动手，让学生看到能玩的东西再反馈\n- 对话示例：学生"接星星"→ 问"直直掉还是飘来飘去？"→ 做 → 展示`;
  }

  // 创作阶段（有代码）
  if (currentCode && currentCode.length < 30000) {
    systemPrompt += `\n\n⚠️ 创作阶段。当前游戏代码在下方。\n
五大规则：
1. 学生是导演，AI是演员：问感受和愿望（"哪里想改？"），不问技术（"要不要改代码？"）
2. 每次只改一个东西，改完立刻展示效果
3. 卡点触发脚手架（连续3次调同一参数→给A/B/C/D选项），解决即消失
4. 每次修改后收束确认："现在这样你满意吗？"
5. 学生不主动看代码就不提代码

当前游戏代码：
\`\`\`html
${currentCode}
\`\`\``;
  }

  const response = await openai.chat.completions.create({
    model: "deepseek-chat",
    messages: [{ role: "system", content: systemPrompt }, ...messages],
    stream: true,
    temperature: 0.8,
  });

  return response;
}

// 保存消息到 Supabase（直接使用 service role，不走 HTTP 回调）
export async function saveMessage(userId: string, role: string, content: string, token: string, sessionId?: string) {
  if (!token) {
    console.error("Save message error: 缺少 token");
    return;
  }

  try {
    // 验证 sessionId 是否为有效的 UUID，否则传 null
    const validUuid = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(sessionId || "");

    const { error } = await supabaseAdmin.from("messages").insert({
      user_id: userId,
      role,
      content,
      session_id: validUuid ? sessionId : null,
    });

    if (error) {
      console.error("Save message error:", error.message);
    }
  } catch (err) {
    console.error("Save message error:", err);
  }
}
